"""
SteelEagle SDK catalog.

The Action/Event/Datatype catalog is GENERATED by introspecting the real
`steeleagle_sdk` registry and cached in `catalog_data.json` (run
`tools/gen_catalog.py` to refresh; `tests/test_catalog_sync.py` guards drift).
This module loads that JSON at import time, so nothing here imports the SDK and
the catalog can never silently diverge from a hand-typed copy.

Key real-grammar facts (from dsl/grammar/dronedsl.lark):
- Values are bare NAME tokens, numbers, arrays, or `_` (None). NO quoted strings.
- NAME = [A-Za-z_][A-Za-z0-9_]*  (no hyphens).
- `Start <action>` in the Mission stanza takes NO colon.
- Nested objects must be pre-declared in Data: and referenced by name.

Two things are NOT generated and live here by hand because they are not
introspectable from the Pydantic field schema:
- CONDITIONAL_REQUIRED: parameters that become required depending on another
  parameter's value (enforced by imperative `@model_validator`s in the SDK,
  e.g. Waypoints `algo = survey`).
"""

from __future__ import annotations
import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Literal


ParamType = Literal["float", "int", "str", "bool", "list", "ref", "any"]


@dataclass
class Param:
    name: str
    type: ParamType
    required: bool = True
    description: str = ""
    enum: list[str] | None = None
    ref_kind: str | None = None
    default: object | None = None


@dataclass
class Entry:
    name: str
    kind: Literal["data", "action", "event"]
    description: str
    params: list[Param] = field(default_factory=list)


# -----------------------------------------------------------------------------
# Load the generated catalog
# -----------------------------------------------------------------------------
_DATA_PATH = Path(__file__).resolve().parent / "catalog_data.json"


def _load() -> tuple[list[Entry], list[Entry], list[Entry], list[str]]:
    raw = json.loads(_DATA_PATH.read_text())

    def to_entry(d: dict) -> Entry:
        return Entry(
            name=d["name"],
            kind=d["kind"],
            description=d.get("description", ""),
            params=[
                Param(
                    name=p["name"],
                    type=p["type"],
                    required=p.get("required", True),
                    description=p.get("description", ""),
                    enum=p.get("enum"),
                    ref_kind=p.get("ref_kind"),
                    default=p.get("default"),
                )
                for p in d.get("params", [])
            ],
        )

    return (
        [to_entry(d) for d in raw.get("datatypes", [])],
        [to_entry(d) for d in raw.get("actions", [])],
        [to_entry(d) for d in raw.get("events", [])],
        list(raw.get("declarable_datatypes", [])),
    )


DATATYPES, ACTIONS, EVENTS, _DECLARABLE = _load()


# -----------------------------------------------------------------------------
# Hand-maintained overlay: conditional-required parameters
# -----------------------------------------------------------------------------
# These cannot be introspected from the field schema; they are enforced by
# imperative `@model_validator`s in the SDK. Keep in sync by hand.
@dataclass
class ConditionalRule:
    when: dict[str, str]      # param name -> required value (bare identifier)
    require: list[str]        # params that become required when `when` matches


CONDITIONAL_REQUIRED: dict[str, list[ConditionalRule]] = {
    "Waypoints": [
        ConditionalRule(when={"algo": "survey"},
                        require=["spacing", "angle_degrees", "trigger_distance"]),
        ConditionalRule(when={"algo": "corridor"},
                        require=["spacing", "angle_degrees"]),
    ],
}


RESERVED_EVENT_NAMES = {"done"}
STANZAS = ("Data", "Actions", "Events", "Mission")


# -----------------------------------------------------------------------------
# Accessors
# -----------------------------------------------------------------------------
def all_entries() -> list[Entry]:
    return DATATYPES + ACTIONS + EVENTS


def find(name: str, kind: str | None = None) -> Entry | None:
    for e in all_entries():
        if e.name == name and (kind is None or e.kind == kind):
            return e
    return None


def actions() -> list[Entry]:
    return ACTIONS


def events() -> list[Entry]:
    return EVENTS


def datatypes() -> list[Entry]:
    """All registered datatypes (validator accepts any of these)."""
    return DATATYPES


def declarable_datatypes() -> list[Entry]:
    """Datatypes a user actually declares in `Data:` (referenced by some
    Action/Event field). Used to keep the LLM prompt focused; the validator
    still accepts any registered datatype via `datatypes()`/`find()`."""
    names = set(_DECLARABLE)
    return [e for e in DATATYPES if e.name in names]


def conditional_rules(class_name: str) -> list[ConditionalRule]:
    return CONDITIONAL_REQUIRED.get(class_name, [])
